#include<iostream>
using namespace std;
int main()
{
    cout<<"The size of integer is : "<<sizeof(int)<<" bytes"<<endl;
    cout<<"The size of long integer is : "<<sizeof(long int)<<" bytes"<<endl;
    cout<<"The size of float is : "<<sizeof(float)<<" bytes"<<endl;
    cout<<"The size of double is : "<<sizeof(float)<<" bytes"<<endl;
    cout<<"The size of character is : "<<sizeof(char)<<" bytes"<<endl;
    cout<<"The size of string is : "<<sizeof(string)<<" bytes"<<endl;
    return 0;
}
